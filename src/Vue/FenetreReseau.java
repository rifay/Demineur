    private String colorInt(int value) {
       String color="";
        switch(value){
            case 1: 
                color = "0000FF";
                break;
            case 2:
                color = "008200";
                break;
            case 3:
                color = "FF0000";
                break;
            case 4:
                color = "000084";
                break;
            case 5:
                color = "8B0000";
                break;
            default:
                color = "008284";
                break;
                
        }
       return "<html><span style=\"font-weight:bold; color:#"+color+";\">"+value+"</span></html>";
    }

