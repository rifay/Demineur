    public FenetreReseau(int niveauPartie) {
        initComponents();
       
        
        switch (niveauPartie) {
            case GridBoard.LVL_FACILE:
                setSize(450,300);
                break;
            case GridBoard.LVL_MOYEN:
                setSize(450,450);
                break;
            case GridBoard.LVL_DIFFICILE:
                setSize(600,450);
                break;
        }
        grille = new GridBoardReseau(niveauPartie);
        height = grille.getHeight();
        lenght = grille.getLenght();
        gridButton = new JButton[height][lenght];
        jPanel_Principal.setLayout(new GridBagLayout());
        jPanel_Principal.applyComponentOrientation(ComponentOrientation.LEFT_TO_RIGHT);

        for (int i = 0; i < height; i++) {
            for (int j = 0; j < lenght; j++) {
                final int x = i;
                final int y = j;
                gridButton[i][j] = new JButton();
                gridButton[i][j].setPreferredSize(new Dimension(15, 15));
                gridButton[i][j].setContentAreaFilled(false);
                gridButton[i][j].setBorder(javax.swing.BorderFactory.createBevelBorder(javax.swing.border.BevelBorder.RAISED));
                gridButton[i][j].setFocusPainted(false);
                gridButton[i][j].setMargin(new java.awt.Insets(1, 1, 1, 1));
                gridButton[i][j].setFont(new java.awt.Font("Tahoma", 0, 10));
                gridButton[i][j].setText(Integer.toString(grille.getGrille()[i][j].getValue()));
                gridButton[i][j].addMouseListener(new MouseAdapter() {
                    @Override
                    public void mousePressed(MouseEvent e) {
                        if (e.getModifiers() == MouseEvent.BUTTON3_MASK) {
                            rightClicCase(x, y);
                        } else if (e.getModifiers() == MouseEvent.BUTTON1_MASK) {
                            System.out.println("clic gauche");
                            leftClicCase(x, y);
                        }

                    }
                });
                GridBagConstraints gbc = new GridBagConstraints();
                gbc.gridx = i;
                gbc.gridy = j;
                jPanel_Principal.add(gridButton[i][j], gbc);

            }
        }
        //grille.startChrono();
        grille.addObserver(this);
        //this.add();
    }

    private void leftClicCase(int x, int y) {

        if (gridButton[x][y].isEnabled()) {
            grille.updateAll(x, y);
        }
    }

    private void rightClicCase(int x, int y) {

        if (gridButton[x][y].isEnabled()) {
            grille.updateFlag(x, y);
        }
    }

